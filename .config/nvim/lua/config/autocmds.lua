vim.api.nvim_create_autocmd("VimResized", {
    pattern = "*",
    desc = "Re-format man buffer when the window is resized.",
    callback = function()
        if vim.bo.filetype == "man" then
            vim.schedule(function() vim.cmd("e") end)
        end
    end,
})

